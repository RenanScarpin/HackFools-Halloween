from colors import *
class Costume:
    def __init__(
        self,
        name: str,
        asciiArt: str,
        artLength: int
    )->None:
        self.name = name
        self.asciiArt = asciiArt
        self.artLength = artLength

    def __str__(self):
        return f"""
{COLORS['purple']}{'~'*self.artLength} {COLORS['yellow']}{self.name.upper()}{COLORS['purple']} {'~'*self.artLength}{RESETCOLOR}
{COLORS['purple']}
{self.asciiArt}
{RESETCOLOR}
{COLORS['purple']}{'~'*self.artLength} {COLORS['yellow']}{self.name.upper()}{COLORS['purple']} {'~'*self.artLength}{RESETCOLOR}
"""

